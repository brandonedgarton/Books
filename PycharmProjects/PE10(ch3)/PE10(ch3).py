
pennies = int(input("Enter the number of pennies to make a dollor: "))
if pennies == 100:
    print("Congratulations! You Win!")
elif pennies > 100:
    print("Amount entered was more than a dollar, you lose!")
elif pennies < 100:
    print("Amount entered was less than a dollar, you lose!")

